#include "application.h"
#include "mainwindow.h"

int main(int argc, char *argv[])
{
    Application app(argc, argv);

    // Ensure only one instance of the application is running
    if (app.isRunning()) {
        return 0;
    }

    MainWindow win;
    win.show();

    return app.exec();
}
